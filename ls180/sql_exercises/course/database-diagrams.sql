-- 1. What are the three levels of schema?
1. Conceptual
2. Logical 
3. Physical 

-- 2. What is a conceptual schema?
High level, general design of a database consisiting of entities represented by rectangles and indicators of relationship types between those entities, represented by arrows. 

-- 3. What is a physical schema?

Physical schema is low lever, detailed database design consisting of a visual representation of entities together with implementation details. 

-- What are the three types of relationships that can be shown in a database diagram?
1. one-to-one
2. one-to-many
3. many-to-many